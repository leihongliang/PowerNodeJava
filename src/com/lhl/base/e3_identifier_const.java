package com.lhl.base;

/*
标识符 有权利自己命名的单词
类名 接口名 :UserSevice
变量名 方法名 :usernamePassword,doSome
常量名 :ABC

数字，字母，下划线，$
*/
public class e3_identifier_const {
    public static void main(String[] args){
        System.out.println(10);         //整数型字面值
        System.out.println(3.14);       //浮点型字面值
        System.out.println(true);       //布尔型字面值
        System.out.println("abc");      //字符串型字面值
        System.out.println('a');        //字符型字面值 单引号只能存放单字符

    }
}
